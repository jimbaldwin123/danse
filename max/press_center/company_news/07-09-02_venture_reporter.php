      <P><A href="http://www.venturereporter.net" target="_blank"><FONT size="1">VentureReporter.net</FONT></A> <FONT size="1"><B>Tuesday, July 09, 2002, 10:21 AM ET</B>
</FONT>      </P>
      <P><SPAN class=head><B>RISING FROM THE DEAD: L90 SURVIVES BY ACQUIRING DOUBLECLICK'S MEDIA BUSINESS</B></SPAN><BR>
      </P>
      <P><I>by Ben Fritz</I></P>
      <P>In what can only be described as a startling return from near-death, online advertising company L90, recently set to be sold to eUniverse for less than its cash on hand has acquired the North American media business of DoubleClick (Nasdaq: DCLK) for $9.5 million, setting itself up as one of the biggest players in the rapidly consolidating online media sales market.</P>
      <P>L90 will pay DoubleClick $5 million in cash and 4.8 million shares for the business, which it will merge with itself into a new company called MaxWorldwide (Nasdaq: MAXW). The combination of the two creates a powerful competitor amongst the remaining online ad networks, such as ValueClick, that compete for Internet ad dollars with portals like Yahoo and MSN.</P>
      <P>At the end of 2001, after years of losses with no profitability in sight, L90 agreed to be acquired by content network eUniverse (Nasdaq: EUNI) for less than its cash on hand, in what could only be considered an admission of defeat. Revelations of accounting improprieties caused that deal to be cancelled though and, as that scandal was investigated, a new management team was brought in to try to revamp the company.</P>
      <P>Mitchell Cannold, appointed this past winter as CEO of L90 and now CEO of MaxWorldwide, says that despite L90's past problems and the general recession in online advertising, he saw his mission to create a leader in the space as clear.</P>
      <P>&quot;The deal with L90 was nothing less than surrender as a transaction and there would have no been no point to my coming on if something like that was the plan,&quot; he told DCR. &quot;I believed from the outset that size mattered and when, in my first week here in early March, DoubleClick called us to discuss this deal, the path was clear.&quot;</P>
      <P>L90 already has a close relationship with DoubleClick since it sold its AdMonitor technology to the New York based online media giant last fall and became the biggest client of its DART ad-serving technology.</P>
      <P>The deal makes DoubleClick, which started off focused exclusively on ad sales, a company wholly devoted to technology and data marketing, save for a 16.1 percent stake in MaxWorldwide, which will be one of its biggest clients.</P>
      <P>MaxWorldwide will be headquartered in New York, bringing together some of DoubleClick's staff there and a small number of employees from L90, previously headquartered in Los Angeles. Significant layoffs are expected as the two companies merge their operations.</P>
      <P>The new company is also planning significant growth through acquisition in both the on- and offline world. Cannold said that one of his strategies to overcome the current Internet slump will be to offer marketing both online and offline. &quot;We're looking at a number of other transactions that give us a full arsenal that distinguishes us from our competitors,&quot; he stated. &quot;The offline list brokerage side of our business has been profitable and we'll be growing that. The portals may be bigger, but we'll be better.&quot;</P>
      <P>Cannold also said pay-per-click search results like Overture's would likely be part of the mix, along with virtually all other types of advertising. Diversity is the main goal for MaxWorldwide, it seems, and with over $40 million left in the bank, it has the resources to continue to grow and provide formidable competition from a company that once looked like little more than a carcass.</P>
      <P>&quot;I'm really proud that we've taken a situation that was as miserable as this one was and in a very short period positioned a company for growth and success,&quot; he observed. &quot;We're totally committed to this and we'll get through this space's growing pains so when the market comes back we'll be standing here ready.&quot;<BR>
      </P>
