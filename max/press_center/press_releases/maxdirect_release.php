            <P><b><font size="4">MAXWORLDWIDE SELLS ITS MAXDIRECT DIVISION TO 
              AMERICAN LIST COUNSEL</font></b><br>
              <BR>
              New York, NY- February 10, 2003 - MaxWorldwide, Inc., (MAXW.PK), 
              the nation's leading independent company in online ad sales and 
              representation and a leader in offline direct marketing, announced 
              today that it has sold its direct marketing division, MaxDirect, 
              to American List Counsel, the industry's leading data marketing 
              service provider. </P>
            <p>American List Counsel (ALC) will acquire all division assets including 
              staff and clients, and will retain MaxDirect's present offices in 
              Valhalla, New York. Britt Vatne, currently Executive Vice President 
              of MaxDirect, will continue to lead the group.</p>
            <p>Mitchell Cannold, President and CEO of MaxWorldwide said, &quot;Our 
              strategy for all divisions of MaxWorldwide is to create scale and 
              service unparalleled in the industry. To thrive in the direct marketing 
              world, size matters. ALC is the biggest and the best so this is 
              a win-win for our customers, employees and shareholders. From here 
              on, we can fully devote all of our resources to maximizing the profitability 
              of our online advertising business including our email marketing 
              business, which is integral to our future growth.&quot;</p>
            <p>&quot;Being a part of the ALC will allow us to offer a full suite 
              of direct marketing services to our clients, &quot; noted Britt 
              Vatne, Executive Vice President of MaxDirect. Joining our team and 
              abilities with the resources and expanded expertise of ALC will 
              give our clients a competitive edge in the marketplace.&quot;</p>
            <p>Donn Rappaport, Chairman and CEO of ALC commented, &quot;Over the 
              past several years we have invested several million dollars in system 
              upgrades, software, and technology tools specifically geared to 
              help our company expand its client services and enhance its productivity. 
              Acquisitions like this one allow us to leverage our infrastructure 
              investment. We are glad to add a successful company like MaxDirect 
              to our family.&quot;</p>
            <p><b>About MaxWorldwide</b><br>
              MaxWorldwide is the leading independent company in online advertising 
              sales and representation and is a leader in traditional direct marketing. 
              MaxWorldwide consists of three divisions - MaxOnline, MaxDirect, 
              and MaxCreative. </p>
            <p>MaxOnline's advertising solutions include media sales across a 
              portfolio of top brand web sites and targeted channels, strategic 
              marketing and creative services and specializes in online direct 
              marketing which includes email and opt-in list management. MaxDirect 
              partners with traditional direct marketing companies and provides 
              innovative list management, alternative media services, and superior 
              data analytics.<br>
              MaxCreative is MaxWorldwide's in-house design group that has a diverse 
              background in online and offline creative development.</p>
            <p>Headquartered in New York City, MaxWorldwide has additional offices<br>
              in Valhalla, NY, Miami, Chicago, MN, Los Angeles, San Francisco, 
              and Seattle. <br>
              For more information please visit our web site:<br>
              <a href="http://www.maxworldwide.com">www.maxworldwide.com</a>.</p>
            <p><b>About American List Counsel</b></p>
            <p>ALC is the industry's leading data marketing organization, providing 
              data acquisition, management and compiling services to the leaders 
              in virtually every business category. Headquartered in Princeton, 
              NJ, ALC operates regional sales, marketing and account management 
              offices in New York, Stamford, CT and San Francisco. The company 
              was included on the INC. 500 list of fastest-growing companies in 
              the country for three consecutive years.<br>
